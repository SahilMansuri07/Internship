//--------------- variables in javascript ----------------- 
//let variable is block scope and mutable we can change its values
let name = "Sahil";
console.log(name);

//const variable is immutable we cannot change its values
const age = 21;
console.log(age);

// var is used in old version of javascript 
var surname = 'mansuri';
console.log(surname);



//-------------- data types in javascript ---------------

// this data types are also called as primitive datatypes
// String datatype
let data_name = "Sahil";
console.log(typeof data_name);

// Number
let data_age = 21;
console.log(typeof data_age);

// Boolean
let isLoggedin = true; 
console.log(typeof data_age);

// Null
let null_data = null;    
console.log(typeof null_data);

// Undefined    
let Undefined_data;
console.log(typeof Undefined_data);

// this data types are also called as none-primitive datatypes
const arr = [1,2,3,4,5,6]

console.log(arr);


